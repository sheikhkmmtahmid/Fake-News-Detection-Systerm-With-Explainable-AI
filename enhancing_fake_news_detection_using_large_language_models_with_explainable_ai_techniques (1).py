    outp = os.path.join(UNIFIED_DIR, "comp_suff_bars.png")
    plt.savefig(outp, bbox_inches="tight", dpi=200); plt.show()
    print("Saved:", outp)

    # Attribution-effect correlation
    corr_rows = []
    for i, text in enumerate(sample_texts[:10], start=1):
        pred_idx, p0 = _combined_pred_idx(text)
        fused_order, fused_scores, _, _, _ = fused_token_scores(i, text, alpha=ALPHA)
        tok_to_score = {t: s for t, s in zip(fused_order, fused_scores)}

        impacts = []
        scores = []
        # single token impact
        for t in fused_order[:50]:  # limit for speed
            x_drop = _mask_tokens_in_text(text, [t], is_word_level=True)
            impact = _combined_prob_for(text, pred_idx) - _combined_prob_for(x_drop, pred_idx)
            impacts.append(float(impact))
            scores.append(abs(tok_to_score[t]))
        if len(scores) >= 3 and np.std(scores) > 0 and np.std(impacts) > 0:
            rho, _ = spearmanr(scores, impacts)
            tau, _ = kendalltau(scores, impacts)
        else:
            rho, tau = np.nan, np.nan
        corr_rows.append({"sentence_id": i, "spearman": float(rho), "kendall_tau": float(tau)})

    df_corr = pd.DataFrame(corr_rows)
    corr_csv = os.path.join(UNIFIED_DIR, "faithfulness_token_corr.csv")
    df_corr.to_csv(corr_csv, index=False)
    print("Saved:", corr_csv)

    plt.figure()
    plt.hist(df_corr["spearman"].dropna().values, bins=8, alpha=0.8)
    plt.title("Token score vs impact Spearman"); plt.xlabel("rho"); plt.ylabel("count")
    outp = os.path.join(UNIFIED_DIR, "token_corr_hist.png")
    plt.savefig(outp, bbox_inches="tight", dpi=200); plt.show()
    print("Saved:", outp)

    # Infidelity
    # Simple text variant: random Bernoulli masks over top M tokens, compare model change to masked score sum
    inf_rows = []
    M = 30
    TRIALS = 50
    q = 0.3
    rng = np.random.RandomState(7)
    for i, text in enumerate(sample_texts[:10], start=1):
        pred_idx, p0 = _combined_pred_idx(text)
        fused_order, fused_scores, _, _, _ = fused_token_scores(i, text, alpha=ALPHA)
        top = fused_order[:min(M, len(fused_order))]
        score_map = {t: float(s) for t, s in zip(fused_order, fused_scores)}
        errs = []
        for _ in range(TRIALS):
            m = rng.rand(len(top)) < q
            masked = [t for t, flag in zip(top, m) if flag]
            x_masked = _mask_tokens_in_text(text, masked, is_word_level=True)
            g = _combined_prob_for(text, pred_idx) - _combined_prob_for(x_masked, pred_idx)
            ssum = sum(score_map[t] for t, flag in zip(top, m) if flag)
            errs.append((g - ssum) ** 2)
        inf_rows.append({"sentence_id": i, "infidelity": float(np.mean(errs))})

    df_inf = pd.DataFrame(inf_rows)
    inf_csv = os.path.join(UNIFIED_DIR, "faithfulness_infidelity.csv")
    df_inf.to_csv(inf_csv, index=False)
    print("Saved:", inf_csv)

    plt.figure()
    plt.bar(df_inf["sentence_id"].astype(int), df_inf["infidelity"].astype(float))
    plt.title("Infidelity per sentence"); plt.xlabel("sentence id"); plt.ylabel("infidelity")
    outp = os.path.join(UNIFIED_DIR, "infidelity_compare.png")
    plt.savefig(outp, bbox_inches="tight", dpi=200); plt.show()
    print("Saved:", outp)

    # One-line summary CSV
    # Group means and CIs
    del_piv = df_del.pivot_table(index="sentence_id", columns="order", values="aopc_del", aggfunc="first")
    out_rows = []
    if "fused" in del_piv:
        m, lo, hi = _mean_ci(del_piv["fused"].values); out_rows.append({"metric": "AOPC_del_fused_mean", "mean": m, "ci_lo": lo, "ci_hi": hi})
    if "random" in del_piv:
        m, lo, hi = _mean_ci(del_piv["random"].values); out_rows.append({"metric": "AOPC_del_random_mean", "mean": m, "ci_lo": lo, "ci_hi": hi})
    if "least" in del_piv:
        m, lo, hi = _mean_ci(del_piv["least"].values); out_rows.append({"metric": "AOPC_del_least_mean", "mean": m, "ci_lo": lo, "ci_hi": hi})

    m, lo, hi = _mean_ci(df_ins["aopc_ins"].values); out_rows.append({"metric": "AOPC_ins_fused_mean", "mean": m, "ci_lo": lo, "ci_hi": hi})

    m, lo, hi = _mean_ci(df_comp[df_comp["k_type"] == "fixed"].groupby("k")["comprehensiveness"].mean().values)
    out_rows.append({"metric": "Comp_fixed_mean", "mean": m, "ci_lo": lo, "ci_hi": hi})
    m, lo, hi = _mean_ci(df_comp[df_comp["k_type"] == "fixed"].groupby("k")["sufficiency"].mean().values)
    out_rows.append({"metric": "Suff_fixed_mean", "mean": m, "ci_lo": lo, "ci_hi": hi})

    m, lo, hi = _mean_ci(df_corr["spearman"].values); out_rows.append({"metric": "Corr_spearman_mean", "mean": m, "ci_lo": lo, "ci_hi": hi})
    m, lo, hi = _mean_ci(df_corr["kendall_tau"].values); out_rows.append({"metric": "Corr_kendall_mean", "mean": m, "ci_lo": lo, "ci_hi": hi})

    m, lo, hi = _mean_ci(df_inf["infidelity"].values); out_rows.append({"metric": "Infidelity_mean", "mean": m, "ci_lo": lo, "ci_hi": hi})

