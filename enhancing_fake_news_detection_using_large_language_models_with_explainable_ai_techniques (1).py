"""Cell 75: Stability Testing (LIME vs FUSED, SHAP vs FUSED)

Inspired from:
https://github.com/marcotcr/lime/blob/master/lime/lime_text.py
https://lime-ml.readthedocs.io/en/latest/lime.html
https://github.com/marcotcr/lime/blob/master/doc/notebooks/Lime%20-%20multiclass.ipynb/
https://shap.readthedocs.io/en/latest/generated/shap.Explanation.html
https://docs.scipy.org/doc/scipy/reference/generated/scipy.stats.spearmanr.html
https://docs.python.org/3/library/itertools.html
https://numpy.org/doc/2.3/reference/generated/numpy.argsort.html
https://stackoverflow.com/questions/46975929/how-can-i-calculate-the-jaccard-similarity-of-two-lists-containing-strings-in-py
"""

SEEDS = [13, 27, 42, 77, 101]
LIME_SAMPLES = 1000
ALPHA = 0.5
TOPKS = [5, 10]

def visible(tok):
    return tok.replace("Ġ","").replace("▁","") or "␣"

def lime_vector(text, model, tok, random_state, num_samples=LIME_SAMPLES):
    # Seed at the EXPLAINER level (correct place for random_state)
    expl = LimeTextExplainer(class_names=class_names, random_state=random_state)

    # also seed NumPy to be extra deterministic
    np.random.seed(random_state)

    exp = expl.explain_instance(
        text,
        lambda x: _predict_proba(model, tok, x),
        num_features=200,
        num_samples=num_samples,
        labels=[np.argmax(_predict_proba(model, tok, [text])[0])]
    )
    pairs = exp.as_list(label=exp.available_labels()[0])
    return {visible(t.lower()): w*100.0 for t, w in pairs}

def shap_vector(text_idx, class_idx):
    eb, er = shap_vals_bert[text_idx], shap_vals_roberta[text_idx]
    vb = eb.values[:, class_idx] if eb.values.ndim==2 else eb.values
    vr = er.values[:, class_idx] if er.values.ndim==2 else er.values
    tb = [visible(t.lower()) for t in eb.data]
    tr = [visible(t.lower()) for t in er.data]
    agg = defaultdict(float)
    for t,v in zip(tb, vb): agg[t] += float(v)
    for t,v in zip(tr, vr): agg[t] += float(v)
    return dict(agg)

def fuse_vectors(lime_map, shap_map, alpha=ALPHA):
    lime_absmax = max(1e-8, max(abs(v) for v in lime_map.values()) if lime_map else 1.0)
    shap_absmax = max(1e-8, max(abs(v) for v in shap_map.values()) if shap_map else 1.0)
    shap_scaled = {k: (v / shap_absmax) * lime_absmax for k,v in shap_map.items()}
    keys = set(lime_map) | set(shap_scaled)
    return {k: alpha*lime_map.get(k,0.0) + (1-alpha)*shap_scaled.get(k,0.0) for k in keys}

def align_to_vector(token_set, token_to_weight):
    return np.array([token_to_weight.get(t, 0.0) for t in token_set], dtype=float)

def topk(tokens, scores, k):
    idx = np.argsort(-np.abs(scores))[:k]
    return set(tokens[i] for i in idx)

def summarize_matrix(M, tokens):
    from itertools import combinations
    from scipy.stats import spearmanr
    rhos = []
    for a,b in combinations(range(M.shape[0]), 2):
        rho, _ = spearmanr(M[a], M[b])
        if np.isfinite(rho): rhos.append(rho)
    mean_rho = float(np.mean(rhos)) if rhos else np.nan

    jacc = {k:[] for k in TOPKS}
    for a,b in combinations(range(M.shape[0]), 2):
        for k in TOPKS:
            Ta = topk(tokens, M[a], k); Tb = topk(tokens, M[b], k)
            inter = len(Ta & Tb); union = len(Ta | Tb) or 1
            jacc[k].append(inter/union)
    j_scores = {k: float(np.mean(v)) if v else np.nan for k,v in jacc.items()}

    var_mean = float(np.mean(np.var(M, axis=0)))
    return mean_rho, j_scores, var_mean

# Stability runner
def stability_scores(i, text, alpha=ALPHA):
    # pick class index
    p_b = _predict_proba(bert_model, bert_tokenizer, [text])[0]; cb = int(np.argmax(p_b))
    shap_map = shap_vector(i-1, cb)

    lime_runs, fused_runs = [], []
    union_tokens = set()
    for seed in SEEDS:
        lb = lime_vector(text, bert_model, bert_tokenizer, seed)
        lr = lime_vector(text, roberta_model, roberta_tokenizer, seed)
        lime_comb = defaultdict(float)
        for d in (lb, lr):
            for k,v in d.items(): lime_comb[k]+=v
        fused = fuse_vectors(lime_comb, shap_map, alpha=alpha)
        #fused = fuse_vectors(lime_comb, shap_map)
        lime_runs.append(lime_comb)
        fused_runs.append(fused)
        union_tokens |= set(lime_comb) | set(fused)

    tokens = sorted(union_tokens)
    L = np.vstack([align_to_vector(tokens, m) for m in lime_runs])
    F = np.vstack([align_to_vector(tokens, m) for m in fused_runs])

    lime_rho, lime_j, lime_var = summarize_matrix(L, tokens)
    fuse_rho, fuse_j, fuse_var = summarize_matrix(F, tokens)

    return {
        "sentence_id": i,
        "lime_spearman": lime_rho, "lime_var": lime_var, **{f"lime_jacc_k{k}":v for k,v in lime_j.items()},
        "fuse_spearman": fuse_rho, "fuse_var": fuse_var, **{f"fuse_jacc_k{k}":v for k,v in fuse_j.items()},
        #**shap_stats
    }

# Verdict thresholds
DELTA_RHO  = 0.02
DELTA_JACC = 0.05
VAR_DROP   = 0.95

def verdict_row(d, baseline="lime"):
    better_rho  = (d["fuse_spearman"] - d[f"{baseline}_spearman"]) >= DELTA_RHO
    better_j5   = (d["fuse_jacc_k5"]  - d[f"{baseline}_jacc_k5"])  >= DELTA_JACC
    better_j10  = (d["fuse_jacc_k10"] - d[f"{baseline}_jacc_k10"])>= DELTA_JACC
    lower_var   = (d["fuse_var"] <= d[f"{baseline}_var"] * VAR_DROP)
    votes = sum([better_rho, better_j5, better_j10, lower_var])
    if votes >= 3: verdict = "Stable"
    elif votes == 2: verdict = "Inconclusive"
    else: verdict = "Unstable"
    return verdict

