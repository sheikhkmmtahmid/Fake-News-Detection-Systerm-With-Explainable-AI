# Combiners (BERT+RoBERTa)
def _visible(tok):
    return tok.replace("Ġ","").replace("▁","")

def _combine_lime(bert_pairs, roberta_pairs, top_k=None):
    """Aggregate by visible token text; sum weights (raw, not %)."""
    agg = defaultdict(float); first_form = {}
    for tok, w in (bert_pairs + roberta_pairs):
        vis = _visible(tok)
        key = vis.lower()
        agg[key] += float(w)
        if key not in first_form:
            first_form[key] = vis if vis else tok
    items = [(first_form[k], v) for k,v in agg.items()]
    items.sort(key=lambda x: abs(x[1]), reverse=True)
    if top_k:
        items = items[:top_k]
    return items

def _combine_shap(tokens_b, values_b, tokens_r, values_r):
    """Aggregate SHAP by visible token; sum scores, keep a representative casing."""
    # Flatten values to 1d (pick predominant class if 2D)
    if isinstance(values_b, np.ndarray) and values_b.ndim==2:
        values_b = values_b[:, int(np.argmax(np.abs(values_b).sum(axis=0)))]
    if isinstance(values_r, np.ndarray) and values_r.ndim==2:
        values_r = values_r[:, int(np.argmax(np.abs(values_r).sum(axis=0)))]

    agg = defaultdict(float); first_form = {}
    for tok, v in list(zip(tokens_b, values_b)) + list(zip(tokens_r, values_r)):
        vis = _visible(str(tok)) or "␣"
        key = vis.lower()
        agg[key] += float(v)
        first_form.setdefault(key, vis)
    # Return tokens & values lists
    toks = [first_form[k] for k in agg.keys()]
    vals = np.array([agg[k] for k in agg.keys()], dtype=float)
    return toks, vals

