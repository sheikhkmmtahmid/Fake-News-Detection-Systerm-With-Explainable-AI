# Doc aggregation helpers (same tokenizer/stride as training)
SW_MAX_LEN = 512
SW_STRIDE  = 128

def window_counts_for_df(df, tokenizer, max_len=SW_MAX_LEN, stride=SW_STRIDE):
    counts = []
    for _, row in df.iterrows():
        enc = tokenizer(
            str(row["sentence"]),
            return_overflowing_tokens=True,
            truncation=True,
            max_length=max_len,
            stride=stride,
            padding="max_length",
            return_tensors="pt",
        )
        counts.append(enc["input_ids"].size(0))
    return counts

def group_logits_by_doc(logits_windows, counts, agg="logit_mean"):
    out = []
    i = 0
    for c in counts:
        chunk = logits_windows[i:i+c]
        if agg == "logit_mean":
            out.append(chunk.mean(axis=0))
        else:
            out.append(chunk.mean(axis=0))
        i += c
    return np.vstack(out)

def softmax_np(z):
    z = z - np.max(z, axis=1, keepdims=True)
    e = np.exp(z)
    return e / np.sum(e, axis=1, keepdims=True)

