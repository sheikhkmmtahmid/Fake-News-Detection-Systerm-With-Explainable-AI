# Check class-index semantics on validation:
# If, for true label == 1, the average logit of column 0 > column 1, that means class-meaning is inverted.
m0_y1 = np.mean(val_logits_w[val_labels_w==1][:,0]) if np.any(val_labels_w==1) else -1e9
m1_y1 = np.mean(val_logits_w[val_labels_w==1][:,1]) if np.any(val_labels_w==1) else -1e9
BERT_SWAP_LOGITS = bool(m0_y1 > m1_y1)
