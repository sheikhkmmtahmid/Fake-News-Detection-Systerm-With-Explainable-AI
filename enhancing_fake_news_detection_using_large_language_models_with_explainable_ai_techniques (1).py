    """
    Single-bar-per-token view by fusing LIME and SHAP:
        fused = alpha * (LIME %) + (1 - alpha) * (SHAP scaled to LIME's % range)
    """
    from collections import defaultdict
    lime_map = { _visible(t).lower(): float(w) for t, w in lime_items }
    shap_map = defaultdict(float)
    shap_arr = np.asarray(shap_values, dtype=float)
    for tok, val in zip(shap_tokens, shap_arr):
        key = _visible(str(tok)).lower() or "␣"
        shap_map[key] += float(val)

    lime_pct = {k: v * 100.0 for k, v in lime_map.items()}
