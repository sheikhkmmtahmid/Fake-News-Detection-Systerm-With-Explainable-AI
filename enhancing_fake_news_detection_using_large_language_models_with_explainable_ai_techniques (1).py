def _compose_two_cols(left_img, right_img, width):
    col_w = (width - PADDING*2 - COL_GAP) // 2
    def fit_w(im):
        if im.width == col_w: return im
        h = int(im.height * (col_w / im.width))
        return im.resize((col_w, h), resample=PILImage.BICUBIC)
    L = fit_w(left_img)
    R = fit_w(right_img)
    row_h = max(L.height, R.height)
    row = PILImage.new("RGB", (width, row_h), BGCOLOR)
    row.paste(L, (PADDING, (row_h - L.height)//2))
    row.paste(R, (PADDING + col_w + COL_GAP, (row_h - R.height)//2))
    return row

