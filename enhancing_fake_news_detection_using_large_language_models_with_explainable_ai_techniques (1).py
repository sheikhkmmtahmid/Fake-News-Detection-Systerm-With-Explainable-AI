def bert_robert_tokenization(dataset):
  sentences = dataset["sentence"].values
  labels = dataset["label"].values
  max_length = 512

  # Tokenize all of the sentences and map the tokens to thier word IDs.
  bert_input_ids = []
  bert_attention_masks = []
  roberta_input_ids = []
  roberta_attention_masks = []

  sentence_ids = []
  counter = 0

  # For every sentence...
  for sent in sentences:
      #encode_plus function will encode the sentences as required by model, including tokenization process and mapping token ids
      bert_encoded_dict = bert_tokenizer.encode_plus(
                          str(sent),        #sentence
                          add_special_tokens = True, # Add '[CLS]' and '[SEP]' tokens
                          max_length = 512,     #Since we have seen from our analysis that almost half of the sentences have length less than 512.
                          padding = 'max_length',    # Pad sentences to 512 length  if the length of sentence is less than max_length
                          return_attention_mask = True,   # Create attention mask
                          truncation = True,  # truncate sentences to 512 length  if the length of sentence is greater than max_length
                          return_tensors = 'pt',     # Return pytorch tensors.
                    )

      roberta_encoded_dict = roberta_tokenizer.encode_plus(
                          str(sent),        #sentence
                          add_special_tokens = True, # Add '[CLS]' and '[SEP]' tokens
                          max_length = 512,        #Since we have seen from our analysis that almost half of the sentences have length less than 512.
                          padding = 'max_length',     # Pad sentences to 512 length  if the length of sentence is less than max_length
                          return_attention_mask = True,   # Create attention mask
                          truncation = True,   # truncate sentences to 512 length  if the length of sentence is greater than max_length
                          return_tensors = 'pt',     # Return pytorch tensors.
                    )


      # Add the encoded sentence to the list.
      bert_input_ids.append(bert_encoded_dict['input_ids'])
      roberta_input_ids.append(roberta_encoded_dict['input_ids'])


      # Add attention mask to the list
      bert_attention_masks.append(bert_encoded_dict['attention_mask'])
      roberta_attention_masks.append(roberta_encoded_dict['attention_mask'])


      # collecting sentence_ids
      sentence_ids.append(counter)
      counter  = counter + 1



  # Convert the lists into tensors.
  bert_input_ids = torch.cat(bert_input_ids, dim=0)
  bert_attention_masks = torch.cat(bert_attention_masks, dim=0)

  roberta_input_ids = torch.cat(roberta_input_ids, dim=0)
  roberta_attention_masks = torch.cat(roberta_attention_masks, dim=0)


  labels = torch.tensor(labels)
  sentence_ids = torch.tensor(sentence_ids)

  return {"Bert":[bert_input_ids, bert_attention_masks, labels], "Roberta":[roberta_input_ids, roberta_attention_masks, labels]}

"""## Sliding-window helpers & config (512-token windows)

Cell 27
"""

SW_MAX_LEN = 512     # window length
SW_STRIDE  = 128     # overlap
SW_TRAIN_CAP = 16    # cap max windows per doc (TRAIN) to reduce long-article bias
SW_VAL_CAP   = 32    # cap for VALIDATION (higher than train for better eval coverage)

def _windows_from_text(tokenizer, text, max_len=SW_MAX_LEN, stride=SW_STRIDE):
    # IMPORTANT: pad to max_length so batches are stackable without a custom collator
    enc = tokenizer(
        str(text),
        return_overflowing_tokens=True,
        truncation=True,
        max_length=max_len,
        stride=stride,
        padding="max_length",        # fixed-length windows (== max_len)
        return_tensors="pt",
    )
    return enc["input_ids"], enc["attention_mask"]

class SlidingWindowTextDataset(Dataset):
    """
    Flattens all documents into token windows.
    Each window is (input_ids, attention_mask, label).
    Caps per-doc windows to avoid long-article dominance.
    Assumes df has columns: 'sentence' and (for labeled splits) 'label'.
    """
    def __init__(self, df, tokenizer, max_len=SW_MAX_LEN, stride=SW_STRIDE,
                 cap_per_doc=None, has_labels=True):
        inputs, attns, labels = [], [], []

        for _, row in df.iterrows():
            input_ids, attn = _windows_from_text(tokenizer, row["sentence"], max_len=max_len, stride=stride)
            n = input_ids.size(0)

            # Randomly cap number of windows per document (anti-bias)
            if cap_per_doc is not None and n > cap_per_doc:
                idx = torch.randperm(n)[:cap_per_doc]
                input_ids = input_ids[idx]
                attn      = attn[idx]

            inputs.append(input_ids)
            attns.append(attn)
            if has_labels:
                labels.append(torch.full((input_ids.size(0),), int(row["label"]), dtype=torch.long))

        self.inputs = torch.cat(inputs, dim=0) if inputs else torch.empty(0, max_len, dtype=torch.long)
        self.attns  = torch.cat(attns,  dim=0) if attns  else torch.empty(0, max_len, dtype=torch.long)
        self.has_labels = has_labels
        self.labels = torch.cat(labels, dim=0) if (has_labels and labels) else None

    def __len__(self):
        return self.inputs.size(0)

    def __getitem__(self, i):
        if self.has_labels and self.labels is not None:
            return self.inputs[i], self.attns[i], self.labels[i]
        else:
            # Dummy label (unused) if labels not present
            return self.inputs[i], self.attns[i], torch.tensor(0, dtype=torch.long)

