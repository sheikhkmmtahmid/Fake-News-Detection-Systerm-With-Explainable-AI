def _draw_badge(draw, text, xy, font, outline,
                fill=(255,255,255), radius=8, pad_x=12, pad_y=6, outline_width=3):
    """
    Draws a rounded rectangle that exactly fits the rendered text with padding.
    """
    x, y = xy
    # Precise text size for THIS font
    left, top, right, bottom = draw.textbbox((x, y), text, font=font)
    rect = (left - pad_x, top - pad_y, right + pad_x, bottom + pad_y)
    draw.rounded_rectangle(rect, radius=radius, fill=fill, outline=outline, width=outline_width)
    draw.text((x, y), text, fill=outline, font=font)

