# Deterministic LIME map for one model
def _lime_map_once(text, model, tok, random_state=42, num_samples=LIME_SAMPLES):
    expl = LimeTextExplainer(class_names=class_names, random_state=random_state)
    exp = expl.explain_instance(
        text,
        lambda x: _predict_proba(model, tok, x),
        num_features=200,
        num_samples=num_samples,
        labels=[int(np.argmax(_predict_proba(model, tok, [text])[0]))]
    )
    pairs = exp.as_list(label=exp.available_labels()[0])
    return {visible(t.lower()): float(w)*100.0 for t, w in pairs}  # LIME in percent

# Deterministic fused scores for a sentence i (using its predicted class)
def fused_token_scores(i, text, alpha=ALPHA):
    # SHAP combined (BERT + RoBERTa) for the predicted class of BERT
    p_b = _predict_proba(bert_model, bert_tokenizer, [text])[0]
    pred_idx = int(np.argmax(p_b))

    # SHAP map
    eb, er = shap_vals_bert[i-1], shap_vals_roberta[i-1]
    vb = eb.values[:, pred_idx] if eb.values.ndim==2 else eb.values
    vr = er.values[:, pred_idx] if er.values.ndim==2 else er.values
    tb = [visible(str(t).lower()) for t in eb.data]
    tr = [visible(str(t).lower()) for t in er.data]
    shap_map = defaultdict(float)
    for t, v in zip(tb, vb): shap_map[t] += float(v)
    for t, v in zip(tr, vr): shap_map[t] += float(v)

    # LIME map (BERT + RoBERTa) and fuse
    lb = _lime_map_once(text, bert_model, bert_tokenizer, random_state=42)
    lr = _lime_map_once(text, roberta_model, roberta_tokenizer, random_state=42)
    lime_comb = defaultdict(float)
    for d in (lb, lr):
        for k, v in d.items(): lime_comb[k] += v

    fused = fuse_vectors(lime_comb, shap_map, alpha=alpha)  # scale SHAP to LIME range inside
    # Build ordered list by absolute fused magnitude
    items = sorted(fused.items(), key=lambda kv: abs(kv[1]), reverse=True)
    tokens = [k for k, _ in items if k.strip() and k != "␣"]
    scores = [float(v) for _, v in items]
    # Also return least-important order for baseline
    items_least = sorted(fused.items(), key=lambda kv: abs(kv[1]))
    tokens_least = [k for k, _ in items_least if k.strip() and k != "␣"]
    return tokens, np.array(scores, float), tokens_least, lime_comb, shap_map

def _combined_pred_idx(text):
    b = _predict_proba(bert_model, bert_tokenizer, [text])[0]
    r = _predict_proba(roberta_model, roberta_tokenizer, [text])[0]
    meanp = (b + r) / 2.0
    return int(np.argmax(meanp)), float(meanp[np.argmax(meanp)])

def _combined_prob_for(text, class_idx):
    b = _predict_proba(bert_model, bert_tokenizer, [text])[0][class_idx]
    r = _predict_proba(roberta_model, roberta_tokenizer, [text])[0][class_idx]
    return float(((b + r) / 2.0))

def _mask_with_order(text, order_tokens, k):
    return _mask_tokens_in_text(text, order_tokens[:k], is_word_level=True)

def _neutral_text(text):
    # Simple neutral baseline: a single space. Keeps tokenizer simple and avoids special tokens.
    return " "

def _mean_ci(a, alpha=0.95):
    a = np.array(a, float)
    m = float(np.nanmean(a))
    if len(a) < 2: return m, np.nan, np.nan
    se = float(np.nanstd(a, ddof=1) / np.sqrt(len(a)))
    from scipy.stats import t
    h = float(se * t.ppf((1.0 + alpha) / 2.0, len(a) - 1))
    return m, m - h, m + h


