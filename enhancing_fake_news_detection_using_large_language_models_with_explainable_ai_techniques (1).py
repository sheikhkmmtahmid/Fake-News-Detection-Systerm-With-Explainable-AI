def _delta_prob(model, tok, text, pred_idx, tokens):
    base = float(_predict_proba(model, tok, [text])[0][pred_idx])
    masked1 = _mask_tokens_in_text(text, tokens[:1]); masked3 = _mask_tokens_in_text(text, tokens[:3])
    p1 = float(_predict_proba(model, tok, [masked1])[0][pred_idx])
    p3 = float(_predict_proba(model, tok, [masked3])[0][pred_idx])
    return base - p1, base - p3, base


